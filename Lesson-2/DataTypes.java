public class DataTypes{
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        System.out.println("The Addition of A is B is:" +(a+b)+"\n");
        
        
        System.out.println("The Subtraction of A is B is:" +(a-b));
        
        System.out.println("The Multiplication of A is B is:" +(a*b));
        
        System.out.println("The Division of A is B is:" +(a/b));

        System.out.println("The Remainder of A is B is:" +(a%b));

        System.out.println("Addition: "+(a+b)+" Subtraction: "+(a-b)+" Multiplication: "+(a*b)+" Division: "+(a/b));
    }
}